/* Matrizes Tipadas 
    Uma 'tupla' é uma matriz tipada com um comprimento e tipos predefinidos para cada índice.

    As tuplas são ótimas porque permitem que cada elemento na matriz seja um tipo conhecido de valor.

    Para definir uma tupla, especifique o tipo de cada elemento na matriz:
*/

let suaTupla: [number, boolean, string];

//inicialização correta
suaTupla = [5, false, "Iniciando tupla corretamente"]
console.log(suaTupla);

